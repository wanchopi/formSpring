package com.wanchopi.spring.model;

/**
 * Student.java
 * @author wanchopi
 *
 */
public class Student {
	
	private String name;
	private String email;
	private String password;
	private String gender;
	private String country;
	private String [] favLanguages;
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the gender
	 */
	public String getGender() {
		return gender;
	}
	/**
	 * @param gender the gender to set
	 */
	public void setGender(String gender) {
		this.gender = gender;
	}
	/**
	 * @return the favLanguages
	 */
	public String[] getFavLanguages() {
		return favLanguages;
	}
	/**
	 * @param favLanguages the favLanguages to set
	 */
	public void setFavLanguages(String[] favLanguages) {
		this.favLanguages = favLanguages;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * @return the country
	 */
	public String getCountry() {
		return country;
	}
	/**
	 * @param country the country to set
	 */
	public void setCountry(String country) {
		this.country = country;
	}
	

}
